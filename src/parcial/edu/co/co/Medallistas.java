package parcial.edu.co.co;

public class Medallistas {
	String nombre,  apellido,tipo, puesto, direccion;
	int numero,  edad,  peso, celular;
	double altura; 
	public Medallistas(String nombre, String apellido, String tipo, int numero, int edad, int peso, double altura, int celular, String direccion, String puesto) {
		this.nombre=nombre;
		this.apellido=apellido;
		this.tipo=tipo;
		this.numero=numero;
		this.edad=edad;
		this.peso=peso;
		this.altura=altura;
		this.celular=celular;
		this.direccion=direccion;
		this.puesto=puesto;
	}
	
	
}
